    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        String sql = "INSERT INTO tiket VALUES (?,?,?,?,?,?,?,?)";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, txtkd.getText());            
            stat.setString(2, cbkelas.getSelectedItem().toString());
            stat.setString(3, cbmas.getSelectedItem().toString());
            stat.setString(4, cbpen.getSelectedItem().toString());
            stat.setString(5, cbtujuan.getSelectedItem().toString());
            stat.setString(6, txttgl.getText());
            stat.setString(7, cbjam.getSelectedItem().toString());
            stat.setString(8, txtbiaya.getText());            
            stat.executeUpdate();
            JOptionPane.showMessageDialog(null,"Data berhasil di simpan");
            kosong();
            txtkd.requestFocus();
        } catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal di simapan"+e);
        }
        datatable();
    }//GEN-LAST:event_bsimpanActionPerformed

